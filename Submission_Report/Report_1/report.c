//  Title : Write a program to calculate and display the volume of a cube having its height (h = 20  cm), width (w = 15 cm), and depth (9 cm).  
 
#include <stdio.h>
int main()
{
   float h= 20, w = 15, d = 9, volume; 
   volume = h * w * d;
   printf("The volume of the cube is %.2f cm\u00b3 \n", volume);
   return 0;
}

